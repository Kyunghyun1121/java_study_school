
public class Player{
	private String name;
	private int age;
	private double average;
	public Player(String n,int a,double ave) {
		name = n;
		age = a;
		 average =ave;
	}
	public String getname() {
		return name;
	}
	public int getage() {
		return age;
	}
	public double getavr() {
		return average;
	}
}
